        <p>Madiun (bahasa Jawa: Hanacaraka: ꦩꦝꦶꦪꦸꦤ꧀, Pegon: مادييون, translit. Madhiyun) adalah sebuah kota di Provinsi Jawa Timur, Indonesia. Di kota ini terdapat Industri kereta api (INKA) dan memiliki sekolah tinggi perkeretaapian, yakni salah satunya Politeknik Perkeretaapian Indonesia. Kota Madiun mendapat julukan sebagai "Kota Gadis", "Kota Brem", "Kota Pecel", "Kota Budaya", "Kota Industri", "Kota Karismatik", dan "Kota Pendekar". Nah Kali ini Mimin mau rekomendasiin nih, dimana aja sih tempat nongkrong yang homie dan tentunya cocok banget  buat anak-anak muda di Madiun.Yuk simak dibawah ini!</p>
        <p>1. Gemar Coffe</p>
        <img src="Gemar.jpeg"width="250px" height="250px">
        <p>Buat kalian penggemar kopi wajib banget datang di Coffe shop satu ini. Selain letaknya yang strategis coffe  shop satu ini cocok banget buat kamu yang suka ngerjain tugas,me time maupun kumpul-kumpul bareng temen.Dekorasi yang aesthetic dengan nuansa perpaduan warna abu-abu dan coklat bikin tambah nyaman banget apalagi banyak spot foto yang bagus. Untuk menu nya sendiri Gemar coffe ada coffe dan non-coffe serta makanan yang enak-enak banget.Gemar Coffee terletak di seberang salah satu pusat perbelanjaan yang ada di wilayah Kota Madiun yakni Suncity Mall Madiun. Tepatnya beralamat di Jalan S. Parman No. 24a, Oro-oro Ombo, Kecamatan Kartoharjo, Kota Madiun.Untuk jam bukanya, buka dari hari Senin hingga Minggu, mulai pukul 08.00-21.00 WIB. </p>
        <p>2. Cafe Bona Boni</p>
        <img src="WhatsApp Image 2022-03-07 at 08.25.11.jpeg" width="250px" height="250px">
        <p>Nah,Siapa nih yang suka banget makan Ice cream? Sekarang Cafe Bona Boni sudah hadir di Kota Madiun loh. Tepatnya di Jalan Eka Jaya Madiun dan jam bukanya yaitu jam 10 pagi. Di Cafe Bona Boni selain menjual Ice cream Gelato yang beraneka ragam, meraka juga menyediakan menu coffe , non-coffe serta brownies yang manis dan lezat. Tempatnya yang homie cocok banget buat kumpul-kumpul bareng temen.</p>
        <p>3.Kohan</p>
        <img src="Kohan.jpeg" width="250px" height="200px">
        <p>Coffe shop yang satu ini gak ketinggalan kerennya. Tempatnya cocok banget buat nongkrong sambil nikmatin live music nya loh. Letaknya juga strategis di tengah-tengah kota Madiun yaitu di Jalan Sulawesi nomor 41, Kartoharjo, Kota Madiun, Jawa Timur. Kohan menyajikan menu coffe dan non-coffe yang beraneka ragam. Buat kamu yang lagi jalan-jalan mengelilingi spot-spot foto yang ada dijalan Pahlawan wajib banget nih mampir kesini.</p>
        <p>4.Memory coffe</p>
        <img src="Memory.jpeg" width="250px" height="250px">
        <p>Yang keempat ada Memory Coffe nih gais. Hayo siapa yang suka banget sama kopi yang pakai teknik manual brew? Nah wajib banget nih datang ke Memory Coffe. Rasa kopinya khas banget. Memory juga menyedikan menu non-coffe juga loh.Untuk harga nya sendiri sangat terjangkau mulai Rp. 12.000. Tempat nya bersih dan higenis. Buat kamu yang suka sama place yang banyak tanemannya cocok banget datang kesini. Alamatnya di Jl. S. Parman No.17, Sukosari, Kec. Kartoharjo, Kota Madiun, Jawa Timur.</p>
